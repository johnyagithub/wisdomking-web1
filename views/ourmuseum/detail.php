									โดยคำนึงถึงความคิดเห็นของประชาชนหรือความต้องการของสาธารณชนด้วย สำหรับวิธีการมีส่วนร่วมพระองค์ทรงนำ
									"ประชาพิจารณ์" มาใช้ในการบริหารจัดการดำเนินงาน ซึ่งเป็นวิธีการที่เรียบง่ายตรงไปตรงมา
									โดยหากจะทำโครงการใดจะทรงอธิบายถึงความจำเป็นและผลกระทบที่เกิดกับประชาชนทุกฝ่าย
									รวมทั้งผู้นำชุมชนในท้องถิ่น
									เมื่อประชาชนในพื้นที่เห็นด้วยแล้ว หน่วยราชการต่างๆ ที่เกี่ยวข้องและร่วมดำเนินการมีความพร้อม
									จึงจะพระราชทานพระราชดำริให้ดำเนินโครงการนั้น ๆ ต่อไป</p>
								<p><b>11.ประโยชน์ส่วนรวม</b> ทรงระลึกถึงประโยชน์ของส่วนรวมเป็นสำคัญ
									เมื่อ พ.ศ. 2514 (เลขไทย) มีพระบรมราโชวาทที่มหาวิทยาลัยขอนแก่น ความตอนหนึ่งว่า “...ใครต่อใครบอกว่า
