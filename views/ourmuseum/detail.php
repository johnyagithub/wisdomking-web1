								<p><b>6.องค์รวม</b> ทรงมองสิ่งต่าง ๆ ที่เกิดอย่างเป็นระบบครบวงจร
									มองทุกสิ่งเป็นพลวัตที่ทุกมิติเชื่อมต่อกัน ในการที่จะพระราชทานพระราชดำริเกี่ยวกับโครงการหนึ่งนั้น
