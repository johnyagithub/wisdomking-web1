									ดังพระราชดำรัสความตอนหนึ่งว่า “...เจ้าหน้าที่ป่าไม้ควรจะปลูกต้นไม้ ลงในใจคนเสียก่อน
									แล้วคนเหล่านั้นก็จะพากันปลูกต้นไม้ลงบนแผ่นดินและรักษาต้นไม้ด้วยตนเอง...”</p>
