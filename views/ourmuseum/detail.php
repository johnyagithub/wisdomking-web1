									แต่ต้องแก้ปวดหัวก่อน เพื่อที่จะให้อยู่ในสภาพที่คิดได้”</p>
								<p><b>4.ทำตามลำดับขั้น</b> “...การพัฒนาประเทศจำเป็นต้องทำตามลำดับขั้น
									ต้องสร้างพื้นฐาน คือ ความพอมีพอกิน พอใช้ของประชาชนส่วนใหญ่เป็นเบื้องต้นก่อน
									โดยใช้วิธีการและใช้อุปกรณ์ที่ประหยัด
