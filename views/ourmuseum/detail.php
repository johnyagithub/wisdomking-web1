									ทรงศึกษาข้อมูลรายละเอียดอย่างเป็นระบบจากข้อมูลพื้นฐานในเบื้องต้นจากเอกสาร แผนที่ สอบถามจากเจ้าหน้าที่
									นักวิชาการ
									และสอบถามจากราษฎรในพื้นที่ให้ได้รายละเอียดที่เป็นประโยชน์ครบถ้วน
									เพื่อที่จะพระราชทานความช่วยเหลือได้อย่างถูกต้องและรวดเร็วตรงความต้องการของประชาชน</p>
								<p><b>2.ระเบิดจากข้างใน</b> หมายความว่า
