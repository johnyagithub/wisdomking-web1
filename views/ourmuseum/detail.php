								<p><b>หลักการทรงงาน 23 ข้อ</b>
									หลักการทรงงานในพระบาทสมเด็จพระบรมชนกาธิเบศร มหาภูมิพลอดุลยเดชมหาราช บรมนาถบพิตร
									ที่ทรงยึดทางสายกลางสอดคล้องกับ สิ่งแวดล้อมและสามารถปฏิบัติได้จริง ทรงมีความละเอียดรอบคอบและทรงคิดค้นหา
